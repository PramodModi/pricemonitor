"""
Contract models between scraper_v2 and PriceMonitor.

ScrapeRequest  — what PriceMonitor sends to the scraper
ScrapeResponse — what the scraper returns

These are the ONLY types scraper_worker.py needs to import from app.scraper_v2.
All internal scraper types (ScrapeResult, PortalConfig, etc.) stay inside
scraper_v2 and never leak into PriceMonitor code.

Both are plain dataclasses — no Pydantic dependency — so they work
whether the scraper runs embedded or as a standalone service.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from decimal import Decimal
from typing import Optional


@dataclass
class ScrapeRequest:
    """
    One scrape request from PriceMonitor to the scraper.

    job_id is generated by PriceMonitor (uuid4 as string) and echoed
    back in ScrapeResponse so price_history rows can be correlated
    with scrape_diagnostics rows.
    """
    url: str
    platform: str     # "amazon" | "flipkart" | "myntra" — must match portals.yaml
    job_id: str       # correlation ID, generated by caller


@dataclass
class ScrapeResponse:
    """
    Result of one scrape attempt.

    On success: success=True, all product fields populated.
    On failure: success=False, error_type + error_message populated,
                product fields are None.

    Diagnostic fields are always populated regardless of outcome —
    they tell PriceMonitor what was tried and how long it took.
    """

    # ── Identity ──────────────────────────────────────────────────────────────
    success: bool
    job_id: str                           # echoed from ScrapeRequest

    # ── Product data (populated on success) ───────────────────────────────────
    marketplace_product_id: Optional[str] = None
    current_price: Optional[Decimal] = None
    name: Optional[str] = None
    brand: Optional[str] = None
    image_url: Optional[str] = None
    availability: bool = True
    rating: Optional[Decimal] = None
    review_count: Optional[int] = None
    seller: Optional[str] = None
    currency: str = "INR"

    # ── Diagnostics (always populated) ────────────────────────────────────────
    portal: str = ""
    extraction_method: Optional[str] = None   # which layer found the price
    layers_attempted: list[str] = field(default_factory=list)
    layers_failed: list[str] = field(default_factory=list)
    total_duration_ms: int = 0
    navigation_ms: int = 0
    extraction_ms: int = 0
    attempt_number: int = 1

    # ── Worker context ────────────────────────────────────────────────────────
    worker_id: Optional[int] = None

    # ── Failure detail (populated on failure) ─────────────────────────────────
    # Matches ScrapeFailureReason constants
    error_type: Optional[str] = None
    error_message: Optional[str] = None


class ScrapeFailureReason:
    """
    Constants for ScrapeResponse.error_type.
    Written to scrape_diagnostics.status and price_history.scrape_status.
    """
    TIMEOUT = "timeout"           # page.goto() timed out
    BOT_DETECTED = "blocked"      # CAPTCHA / challenge page
    ALL_LAYERS_FAILED = "failed"  # page loaded, no price found
    CONFIG_ERROR = "config_error" # portal not in portals.yaml
